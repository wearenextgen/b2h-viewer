def normalized_product_text(slug):
    content = "\n".join(path.read_text(encoding="utf-8") for path in sorted((ROOT / "products" / slug).glob("*.html")))
    content = unescape(content)
    return re.sub(r"\s+", " ", content).casefold()


def main():
    failed = False
    for slug, phrases in REQUIRED.items():
        haystack = normalized_product_text(slug)
        missing = [phrase for phrase in phrases if re.sub(r"\s+", " ", phrase).casefold() not in haystack]
        if missing:
            failed = True
            print(f"FAIL {slug}")
            for phrase in missing:
                print(f"  missing: {phrase}")
        else:
            print(f"PASS {slug}: {len(phrases)} required source markers found")
    return int(failed)


if __name__ == "__main__":
    sys.exit(main())
